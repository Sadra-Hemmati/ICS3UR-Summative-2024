package CupcakeClicker.src.Game;

/**
 * Enum representing different types of upgrades in the game.
 */
public enum UpgradeType {
    /**
     * Upgrades that increase cupcakes per second (CPS).
     */
    CPS,
    
    /**
     * Upgrades that increase cupcakes per click (CLICK).
     */
    CLICK,
    
    /**
     * Upgrades that increase the prestige multiplier.
     */
    PRESTIGE,
    
    /**
     * Upgrades that affect generators.
     */
    GENERATOR,
}
